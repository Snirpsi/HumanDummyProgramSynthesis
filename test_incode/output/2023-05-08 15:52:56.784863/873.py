#!/usr/bin/python3
numbers = [1,2,3]
if __name__ == '__main__':
    #A function that enumerates numbers.
    #The function takes no arguments.
    #The function returns an enumerated list.
    #The enumerated list contains each number in the list.
    #The enumerated list can be accessed using the enumerate function.
    #The enumerate function returns an enumerated list.
    enumerated_list = enumerate(numbers)
    print(enumerated_list)

