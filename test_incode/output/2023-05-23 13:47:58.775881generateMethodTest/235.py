words = ["hello","world","!"]
if __name__ == '__main__':
    #A endless loop that removes words.
    while True:
        word = input("Enter a word: ")
        if word in words:
            words.remove(word)
        else:
            print("That word is not in the list")
            break
    #Prints the remaining words.
    for word in words:
        print(word)
