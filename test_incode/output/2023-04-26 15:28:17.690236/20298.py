#!/usr/bin/python3
if __name__ == '__main__':
    """ A minimal webserver that stores words and enumerates numbers. """    
    
    import sys
    
    if len(sys.argv) < 2:
        print('usage: python wordenum.py <wordfile>')
        sys.exit()
    
    wordfile = sys.argv[1]
    
    words = []
    
    with open(wordfile) as fp:
        for line in fp:
            word = line.strip()
            words.append(word)
    
    numbers = []
    
    with open(wordfile) as fp:
        for line in fp:
            number = int(line.strip())
            numbers.append(number)
    
    words.sort()
    numbers.sort()
    
    print('\n'.join(words))
    print('\n'.join(numbers))
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
