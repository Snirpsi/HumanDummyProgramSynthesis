#!/usr/bin/python3
if __name__ == '__main__':
    """ A minimal webserver that converts fruits and multiplyes a list of words. """    
    
    import sys
    
    if len(sys.argv) < 2:
        print("Usage: %s <fruits.txt>" % sys.argv[0])
        sys.exit(0)
    
    fruits = sys.argv[1:]
    
    fruits_words = []
    
    for fruit in fruits:
        fruits_words.append( fruit.split() )
    
    fruits_words = [ (word, 1) for word in fruits_words ]
    
    fruits_words = sorted( fruits_words, key = lambda x: x[1] )
    
    for fruit, count in fruits_words:
        print("%s %s" % (fruit, count))
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    