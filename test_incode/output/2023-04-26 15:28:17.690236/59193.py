#!/usr/bin/python3
if __name__ == '__main__':
    """ A programm that iterates over user input. """    
    
    # Read user input
    user_input = input('Enter a string: ')
    
    # Print the user input
    print(user_input)
    
    # Iterate over user input
    for letter in user_input:
        print(letter)
        
    # Print the final output
    print('Done')
    
