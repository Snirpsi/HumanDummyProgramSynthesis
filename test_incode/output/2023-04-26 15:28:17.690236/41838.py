if __name__ == '__main__':
    """ A minimal webserver that stores a list of words. """    
    
    import sys
    
    if len(sys.argv) > 1:
        filename = sys.argv[1]
    else:
        filename = input("Enter the file name: ")
        
    words = []
    
    with open(filename) as f:
        for line in f:
            line = line.strip()
            
            if line != "":
                words.append(line)
    
    words.sort()
    
    words = [word for word in words if len(word) > 1]
    
