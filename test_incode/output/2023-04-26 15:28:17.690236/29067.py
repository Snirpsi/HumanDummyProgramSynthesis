#!/usr/bin/python3
if __name__ == '__main__':
    """ A function that converts user input or returns numbers. """    
    
    # Convert user input to a number
    try:
        number = float(input('Enter a number: '))
    except ValueError:
        print('Invalid input')
    else:
        print('The number you entered is {}'.format(number))
    
    # Return number
    return number
    
# Call main function
main()
