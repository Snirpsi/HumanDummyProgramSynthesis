if __name__ == '__main__':
    """ A endless loop that removes a list of words and prints fruits. """    
    words = ['apple', 'banana', 'cherry']
    fruits = []
    while True:
        fruits.append(input('Enter a fruit: '))
        fruits.append(input('Enter another fruit: '))
        if fruits == words:
            break
    print('The fruits are', fruits)
