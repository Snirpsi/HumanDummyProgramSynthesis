if __name__ == '__main__':
    """ A endless loop that stores a list of words or returns fruits. """    
    words = []
    while True:
        fruits = input("Enter a word or type 'quit' to quit: ")
        if fruits == 'quit':
            break
        else:
            words.append(fruits)
    print(words)
