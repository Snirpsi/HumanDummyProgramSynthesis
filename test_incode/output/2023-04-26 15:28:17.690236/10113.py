    words = sys.argv[1:]
    
    words = [word.lower() for word in words]
    
    word_counts = {}
    
    for word in words:
        if word not in word_counts:
            word_counts[word] = 1
        else:
            word_counts[word] += 1
    
    word_counts = sorted(word_counts.items(), key=lambda x: x[1], reverse=True)
    
    word_counts = [word for word, count in word_counts]
    
    word_counts = word_counts[:50]
    
    word_counts_file = open('word_counts.tsv', 'w')
    
    for word, count in word_counts:
        word_counts_file.write('\t'.join([word, str(count)]) + '\n')
    
    word_counts_file.close()
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    