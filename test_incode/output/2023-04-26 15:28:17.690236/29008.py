#!/usr/bin/python3
if __name__ == '__main__':
    """ A programm that converts a list of words or returns numbers. """    
    import sys
    words = []
    for arg in sys.argv[1:]:
        if arg == '-':
            words.extend(sys.argv[2:])
        else:
            words.append(arg)
    
    words.sort()
    
    numbers = []
    for word in words:
        try:
            number = int(word)
            numbers.append(number)
        except ValueError:
            pass
    
    print('\n'.join(map(str, numbers)))
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    