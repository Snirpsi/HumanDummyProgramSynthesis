#!/usr/bin/python3
if __name__ == '__main__':
    """ A programm that removes a list of words and adds a list of words. """    
    import sys
    
    if len(sys.argv) != 2:
        print("Usage: python3 remove_words_from_file.py <file> [<words>]")
        sys.exit()
    
    file = open(sys.argv[1], 'r')
    words = sys.argv[2:]
    
    file.close()
    
    words = words.split(',')
    
    file = open(sys.argv[1], 'w')
    
    for word in words:
        file.write(word + '\n')
    
    file.close()
    
    print("Done")
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
