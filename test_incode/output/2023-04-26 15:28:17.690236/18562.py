#!/usr/bin/python3
if __name__ == '__main__':
    """ A programm that removes a list of numbers and enumerates all ports. """    
    
    import sys
    
    if len(sys.argv) < 2:
        print("Usage: %s <list>" % sys.argv[0])
        sys.exit()
    
    ports = sys.argv[1:]
    
    ports = list(map(int, ports))
    
    ports_set = set(ports)
    
    ports_removed = list(ports_set - set(ports))
    
    print("Removed ports:")
    for port in ports_removed:
        print("  %s" % port)
    
    print("Enumerated ports:")
    for port in ports:
        print("  %s" % port)
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
