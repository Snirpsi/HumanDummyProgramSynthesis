#!/usr/bin/python3
if __name__ == '__main__':
    """ A programm that opens a list of numbers and multiplyes a list of words. """    
    
    numbers = input("Enter a list of numbers: ")
    words = input("Enter a list of words: ")
    
    numbers = list(map(int, numbers.split()))
    words = list(map(str.strip, words.split()))
    
    numbers_multiplied = []
    words_multiplied = []
    
    for number in numbers:
        numbers_multiplied.append(number * words[number])
    
    for word in words:
        words_multiplied.append(word * numbers_multiplied[words.index(word)])
    
    print("\n".join(map(str, words_multiplied)))
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
