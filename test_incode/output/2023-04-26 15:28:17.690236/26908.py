    """ A programm that removes numbers. """    
    
    import sys
    
    if len(sys.argv) < 2:
        print("Usage: python3 remove_numbers.py <filename>")
        exit()
    
    filename = sys.argv[1]
    
    with open(filename) as f:
        lines = f.readlines()
        
    for line in lines:
        line = line.strip()
        
        if line:
            number = int(line)
            
            if number > 0:
                number = number - 1
                
                sys.stdout.write(str(number))
                
            else:
                sys.stdout.write("\n")
                
