#!/usr/bin/python3
if __name__ == '__main__':
    """ A programm that adds a list of numbers and converts fruits. """    
    fruits = ['apple', 'orange', 'banana', 'cherry']
    fruits_sum = 0
    for fruit in fruits:
        fruits_sum += fruit
    print('The sum of all fruits is {}.'.format(fruits_sum))
