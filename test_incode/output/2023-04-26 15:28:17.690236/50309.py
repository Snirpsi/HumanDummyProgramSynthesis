if __name__ == '__main__':
    """ A function that returns fruits and multiplyes a list of words. """    
    fruits = ['apple', 'banana', 'cherry', 'orange', 'kiwi']
    words = ['apple', 'banana', 'cherry', 'orange', 'kiwi']
    fruits_multiplied = []
    for fruit in fruits:
        fruits_multiplied.append(fruit)
    for word in words:
        fruits_multiplied.append(fruit*word)
    print(fruits_multiplied)
