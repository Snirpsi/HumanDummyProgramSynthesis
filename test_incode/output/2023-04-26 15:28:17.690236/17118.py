#!/usr/bin/python3
if __name__ == '__main__':
    """ A function that enumerates user input. """    
    
    choice = input("Enter your choice: ")
    
    if choice == "a":
        print("A")
    elif choice == "b":
        print("B")
    elif choice == "c":
        print("C")
    elif choice == "d":
        print("D")
    else:
        print("Invalid choice")
        
