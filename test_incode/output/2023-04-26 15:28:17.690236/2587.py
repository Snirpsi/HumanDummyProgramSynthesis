#!/usr/bin/python3
if __name__ == '__main__':
    """ A endless loop that enumerates words and adds a list of numbers. """    
    words = []
    numbers = []
    while True:
        word = input("Enter a word: ")
        if word == 'quit':
            break
        numbers.append(int(input("Enter a number: ")))
        words.append(word)
    print("Words: ", words)
    print("Numbers: ", numbers)
    print("End of program")
