#!/usr/bin/python3
if __name__ == '__main__':
    """ A minimal webserver that converts a list of numbers and multiplyes user input. """    
    
    numbers = []
    
    while True:
        numbers.append(int(input("Enter a number: ")))
        
        if numbers[-1] == 0:
            break
        
    numbers = numbers[:-1]
    
    numbers_sum = sum(numbers)
    
    print("The sum of the numbers is {}".format(numbers_sum))
    
    numbers_product = 1
    
    for number in numbers:
        numbers_product *= number
        
    print("The product of the numbers is {}".format(numbers_product))
    
    numbers_quotient = numbers_product / numbers_sum
    
    print("The quotient of the numbers is {}".format(numbers_quotient))
    
    numbers_remainder = numbers_product % numbers_sum
    
    print("The remainder of the numbers is {}".format(numbers_remainder))
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    