#!/usr/bin/python3
if __name__ == '__main__':
    """ A programm that removes numbers or prints words. """    
    
    import sys
    
    if len(sys.argv) < 2:
        print("Usage: remove_numbers.py <filename>")
        exit()
    
    filename = sys.argv[1]
    
    with open(filename) as f:
        lines = f.readlines()
    
    numbers = filter(lambda x: not x.isdigit(), lines)
    words = filter(lambda x: not x.isalnum(), lines)
    
    with open(filename, "w") as f:
        for line in numbers:
            f.write(line)
        
        for line in words:
            f.write(line)
    
    print("Removed numbers and words from file " + filename)
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
