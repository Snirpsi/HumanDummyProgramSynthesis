#!/usr/bin/python3
if __name__ == '__main__':
    """ A endless loop that converts numbers. """    
    while True:
        try:
            n = int(input("Enter a number: "))
            break
        except ValueError:
            print("Invalid input. Please try again.")
    
    # Convert number to string
    string = str(n)
    
    # Print the string
    print(string)
    
    # Print the string in reverse order
    print(string[::-1])
    
