    """ A function that removes all ports or removes a list of numbers. """    
    
    ports = sys.argv[1:]
    
    if len(ports) == 0:
        print("Usage: python3 remove-ports.py <ports>")
        exit(1)
    
    ports = ports.split(',')
    
    for port in ports:
        
        port = int(port)
        
        if port < 0 or port > 65535:
            print("Error: Port number must be between 0 and 65535.")
            exit(1)
        
        if port in ports:
            ports.remove(port)
            
    if len(ports) == 0:
        print("No ports left after removal.")
        exit(1)
    
    print("Removed ports: ", ports)
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    