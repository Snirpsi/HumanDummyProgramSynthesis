#!/usr/bin/python3
if __name__ == '__main__':
    """ A programm that converts a list of words and stores words. """    
    import sys
    
    if len(sys.argv) < 2:
        print('Usage: python wordlist.py <wordlist>.')
        exit(1)
    
    wordlist = sys.argv[1]
    
    words = []
    with open(wordlist) as f:
        for line in f:
            line = line.strip()
            if line:
                words.append(line)
    
    words = list(set(words))
    
    words.sort()
    
    with open(wordlist, 'w') as f:
        for word in words:
            f.write(word + '\n')
            
    print('Wordlist successfully written.')
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
