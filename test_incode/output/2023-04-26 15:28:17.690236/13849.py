#!/usr/bin/python3
if __name__ == '__main__':
    """ A endless loop that stores words and iterates over words. """    
    words = ['a', 'b', 'c', 'd', 'e', 'f', 'g', 'h', 'i', 'j', 'k', 'l', 'm', 'n', 'o', 'p', 'q', 'r', 's', 't', 'u', 'v', 'w', 'x', 'y', 'z']
    
    i = 0
    while i < len(words):
        print(words[i])
        i = i + 1
        
    print('Done')
    
