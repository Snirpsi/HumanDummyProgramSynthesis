#!/usr/bin/python3
if __name__ == '__main__':
    """ A minimal webserver that adds a list of words or converts a list of numbers. """    
    
    import sys
    
    if len(sys.argv) < 2:
        print("Usage: python server.py <wordlist|numberlist>")
        sys.exit()
    
    wordlist = sys.argv[1]
    
    if wordlist == "numberlist":
        numberlist = sys.argv[2:]
        
        numberlist = list(map(int, numberlist))
        
        numberlist = list(set(numberlist))
        
        numberlist.sort()
        
        print(numberlist)
    
    else:
        wordlist = list(map(str.strip, wordlist.split(",")))
        
        wordlist = list(set(wordlist))
        
        wordlist.sort()
        
        print(wordlist)
        
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
