#!/usr/bin/python3
if __name__ == '__main__':
    """ A function that prints a port or converts fruits. """    
    
    fruits = ['apple', 'banana', 'orange', 'kiwi', 'pear']
    
    fruits_converted = []
    
    for fruit in fruits:
        if fruit in fruits_converted:
            fruits_converted.append(fruit)
        else:
            fruits_converted.append(fruit)
            
    print('\n'.join(fruits_converted))
    
