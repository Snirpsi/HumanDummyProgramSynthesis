if __name__ == '__main__':
    """ A function that adds all ports and calculates fruits. """    
    
    # Read ports file
    ports = open('ports.txt', 'r')
    ports_content = ports.readlines()
    ports.close()
    
    # Add all ports
    ports_content = [x.strip() for x in ports_content]
    ports_content.sort()
    
    # Calculate fruits
    fruits = []
    for x in ports_content:
        fruits.append(x)
    fruits.sort()
    
    # Print results
    print('Fruits:')
    for x in fruits:
        print(x)
        
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    