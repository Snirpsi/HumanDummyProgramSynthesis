#!/usr/bin/python3
if __name__ == '__main__':
    """ A endless loop that opens a list of numbers. """    
    numbers = []
    while True:
        numbers.append(int(input('Enter a number: ')))
        if len(numbers) == 3:
            break
    print('The numbers are: ', end='')
    for number in numbers:
        print(number, end='')
    print('')
