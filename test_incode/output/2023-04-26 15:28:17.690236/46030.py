#!/usr/bin/python3
if __name__ == '__main__':
    """ A minimal webserver that converts a list of words and removes a list of numbers. """    
    import sys
    
    if len(sys.argv) < 3:
        print("Usage: %s <wordlist> <numberlist>" % (sys.argv[0]))
        sys.exit()
    
    wordlist = sys.argv[1]
    numberlist = sys.argv[2]
    
    wordlist = wordlist.split()
    numberlist = numberlist.split()
    
    wordlist = [x.lower() for x in wordlist]
    numberlist = [int(x) for x in numberlist]
    
    wordlist = [x for x in wordlist if x not in numberlist]
    
    wordlist = " ".join(wordlist)
    
    print(wordlist)
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
