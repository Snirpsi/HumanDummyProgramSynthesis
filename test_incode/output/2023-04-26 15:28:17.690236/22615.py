if __name__ == '__main__':
    """ A endless loop that stores words and calculates all ports. """    
    words = []
    ports = []
    
    while True:
        word = input('Enter a word: ')
        
        if word == 'quit':
            break
        
        if word not in words:
            words.append(word)
            ports.append(len(words))
            
            print('The word %s has %d ports.' % (word, len(words)))
            
        else:
            print('That word is already in use.')
            
    print('The word %s has %d ports.' % (words[0], len(words)))
    
    for word in words:
        print('The word %s has %d ports.' % (word, ports[word]))
        
    print('Thank you for using my word counting program!')
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    