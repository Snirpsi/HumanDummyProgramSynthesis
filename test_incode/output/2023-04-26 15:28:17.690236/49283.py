#!/usr/bin/python3
if __name__ == '__main__':
    """ A programm that enumerates a list of words and multiplyes numbers. """    
    import sys
    
    if len(sys.argv) < 2:
        print('Usage: python wordcount.py words.txt')
        exit(1)
    
    with open(sys.argv[1]) as f:
        words = [word.strip() for word in f]
    
    numbers = []
    for word in words:
        numbers.append(int(word))
    
    numbers = sum(numbers)
    
    print('The numbers of words in %s are: ' % sys.argv[1], numbers)
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
