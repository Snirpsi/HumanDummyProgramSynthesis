#!/usr/bin/python3
if __name__ == '__main__':
    """ A programm that converts all ports or iterates over a list of numbers. """    
    
    import sys
    
    if len(sys.argv) < 2:
        print("Usage: python ports_to_numbers.py <ports>")
        sys.exit()
    
    ports = sys.argv[1:]
    
    for port in ports:
        port = int(port)
        
        if port < 0 or port > 65535:
            print("Port must be between 0 and 65535.")
            sys.exit()
        
        if port == 0 or port == 65535:
            print("Port must be 0 or 65535.")
            sys.exit()
        
        if port == 65535:
            print("Port must be 65535.")
            sys.exit()
        
        ports.append(port)
        
    print(ports)
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    