#!/usr/bin/python3
if __name__ == '__main__':
    """ A programm that multiplyes a list of numbers or enumerates a list of words. """    
    
    import sys
    
    if len(sys.argv) != 2:
        print("Usage: python %s <filename>" % sys.argv[0])
        sys.exit(1)
    
    filename = sys.argv[1]
    
    lines = open(filename, 'r').readlines()
    
    words = []
    
    for line in lines:
        line = line.strip()
        
        if line == '':
            continue
        
        words.append(line)
    
    words = words[:-1]
    
    words.sort()
    
    multiplier = 1
    
    for line in words:
        multiplier = multiplier * int(line)
    
    print("The product of %d words is %d." % (len(words), multiplier))
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    