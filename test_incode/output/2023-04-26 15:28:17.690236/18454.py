#!/usr/bin/python3
if __name__ == '__main__':
    """ A endless loop that stores fruits. """    
    while True:
        fruit = input("Enter a fruit: ")
        if fruit == "apple":
            print("Oh no! The fruit is apple!")
        elif fruit == "banana":
            print("Oh no! The fruit is banana!")
        elif fruit == "cherry":
            print("Oh no! The fruit is cherry!")
        elif fruit == "orange":
            print("Oh no! The fruit is orange!")
        elif fruit == "pineapple":
            print("Oh no! The fruit is pineapple!")
        elif fruit == "strawberry":
            print("Oh no! The fruit is strawberry!")
        elif fruit == "watermelon":
            print("Oh no! The fruit is watermelon!")
        else:
            print("Sorry, I didn't understand your fruit.")
