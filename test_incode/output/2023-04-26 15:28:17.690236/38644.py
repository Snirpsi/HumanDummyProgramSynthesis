#!/usr/bin/python3
if __name__ == '__main__':
    """ A function that enumerates user input. """    
    
    choice = input('Please select an option: ')
    
    if choice == '1':
        print('Option 1')
    elif choice == '2':
        print('Option 2')
    elif choice == '3':
        print('Option 3')
    elif choice == '4':
        print('Option 4')
    elif choice == '5':
        print('Option 5')
    else:
        print('Invalid input')
    
