#!/usr/bin/python3
if __name__ == '__main__':
    """ A function that enumerates user input. """    
    
    choice = input("Enter your choice: ")
    
    if choice == "a":
        print("a")
    elif choice == "b":
        print("b")
    elif choice == "c":
        print("c")
    elif choice == "d":
        print("d")
    else:
        print("Invalid choice")
        
