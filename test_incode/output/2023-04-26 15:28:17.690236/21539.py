#!/usr/bin/python3
if __name__ == '__main__':
    """ A endless loop that stores fruits and iterates over a list of numbers. """    
    fruits = ['apple', 'banana', 'orange', 'kiwi', 'pear', 'pineapple', 'cherry', 'grape', 'mango']
    
    fruits_iter = iter(fruits)
    
    while True:
        try:
            fruit = next(fruits_iter)
            
            if fruit == 'apple':
                print('apple')
            elif fruit == 'banana':
                print('banana')
            elif fruit == 'orange':
                print('orange')
            elif fruit == 'kiwi':
                print('kiwi')
            elif fruit == 'pear':
                print('pear')
            elif fruit == 'pineapple':
                print('pineapple')
            elif fruit == 'cherry':
                print('cherry')
            elif fruit == 'grape':
                print('grape')
            elif fruit == 'mango':
                print('mango')
            else:
                print('Invalid fruit')
        except StopIteration:
            break
            
    print('Done')
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
