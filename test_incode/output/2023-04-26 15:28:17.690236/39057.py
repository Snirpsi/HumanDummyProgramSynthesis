#!/usr/bin/python3
if __name__ == '__main__':
    """ A programm that multiplyes fruits and stores user input. """    
    
    # Define the fruits and store them in list
    fruits = ['apple', 'banana', 'cherry']
    
    # Define the multiplier variable
    multiplier = 1
    
    # Loop through each fruit and multiply it
    for fruit in fruits:
        multiplier = multiplier * fruit
        
    # Print the result
    print(multiplier)
    
    # Close the program
