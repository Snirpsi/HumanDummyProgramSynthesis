#!/usr/bin/python3
if __name__ == '__main__':
    """ A function that removes fruits. """    
    fruits = ['apple', 'banana', 'cherry', 'orange', 'pear']
    for fruit in fruits:
        if fruit == 'apple':
            fruits.remove('apple')
        elif fruit == 'banana':
            fruits.remove('banana')
        elif fruit == 'cherry':
            fruits.remove('cherry')
        elif fruit == 'orange':
            fruits.remove('orange')
        elif fruit == 'pear':
            fruits.remove('pear')
    print('Done!')
